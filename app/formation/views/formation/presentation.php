						<p><strong>Aide aux chorales </strong><br/>
Il s'agit de 4h30 de formation gratuite dispensée par un intervenant de l’A.R.P.A. avec des conseils techniques adaptés au répertoire travaillé. </p>

<p><strong>Aide aux projets</strong><br/>
Si vous avez un projet artistique fort, si vous souhaitez élaborer un plan de formation spécifique pour votre chœur, l’ARPA met à disposition un intervenant et vous aide dans votre démarche.
Un entretien préalable permettra de définir les objectifs généraux de la formation, le contenu pédagogique, le volume et le calendrier des interventions. </p>

<p><strong>Conseil et Expertise "sur mesure"</strong><br/>
Les dernières saisons démontrent un intérêt croissant des chœurs et chefs de chœur pour des conseils adaptés. Vous vous posez des questions sur votre pratique en général et plus particulièrement sur vos techniques de répétitions l’étude de votre répertoire, le niveau vocal et musical de votre groupe, la définition d’un nouveau projet... 
Nous vous proposons de vous accompagner dans l’excercice quotidien de votre pratique, dans la mesure des disponibilités des intervenants et de vous mettre en relation avec une personne ressource pour vous faire profiter de notre expérience. </p>

<p><strong>Financement</strong><br/>
Le coût de la formation peut-être pris en charge en partie selon chaque département. Le dispositif habituel consiste en la répartition des frais liés à l’intervention "prorata temporis" à égalité entre l’ARPA, le chœur et votre relais départemental. Les frais de déplacement de l’intervenant sont à la charge du chœur. </p>

<p><strong>Modalités</strong><br/>
Pour bénéficier de ces 3 types de formations vous devez faire une demande par écrit avant le 30 novembre de chaque année auprès de votre relais départemental. Les interventions se dérouleront dans la mesure du possible dans le courant du 1er semestre de l’année civile suivante. Vous devez également acquitter "l’adhésion chorale" de 60€ à l’ARPA qui assurera la gestion administrative et sociale des intervenants. </p>
